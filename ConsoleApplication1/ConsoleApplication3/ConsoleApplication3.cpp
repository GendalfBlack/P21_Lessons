// Задача 2. Написати функцію що приймає рядок символів та повертає
// довжину цього рядка. Рядок символів у форматі const char*.

int lenght(const char* str) {
    int n = 0;
	while ( str[n] != '\0') // цикл допоки в рядку не зустрі-
							// немо символ '\0' що показує
							// кінець рядка
	{
		n++;
	}
	return n;
}

